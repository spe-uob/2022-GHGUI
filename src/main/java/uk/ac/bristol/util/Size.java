package uk.ac.bristol.util;

/** The size of the Window. */
public class Size {
  /** Dimensions for this size object. */
  private final double width, height;
  /**
   * Construct a size object.
   *
   * @param x width
   * @param y height
   */
  public Size(final double x, final double y) {
    width = x;
    height = y;
  }
}
